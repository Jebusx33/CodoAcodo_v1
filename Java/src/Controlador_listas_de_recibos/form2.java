    public int reci=1;
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //se manda losdatos de la fila seleccionanda a un Jtable
        int select= jTable2.getSelectedRow();
       
        try {
            if (select==-1) {
                JOptionPane.showMessageDialog(null, "PORFAVOR SELECIONE UNA FILA", "ADVERTENCIA",JOptionPane.WARNING_MESSAGE);
                
            }else{
            String produ="";
            int subtotal,saco,sugerido,pesoneto,pesobruto;
                 produ=jTable2.getValueAt(select, 1).toString();
                pesoneto=Integer.parseInt(jTable2.getValueAt(select, 2).toString());
                pesobruto=Integer.parseInt(jTable2.getValueAt(select, 3).toString());
                sugerido=Integer.parseInt(jTable2.getValueAt(select, 4).toString());
                saco=Integer.parseInt(jTable2.getValueAt(select, 5).toString());
                subtotal=saco*sugerido;
                
            Object file[]={reci,produ,saco,pesobruto,pesoneto,sugerido,subtotal};
                z.tablaRemicion.addRow(file);
                reci++;
                //En vialos datos al form 3 en el form3 titnten que esta los jTextField como public static
               form3.jTextField7.setText(String.valueOf(totalpesoBruto()));
               form3.jTextField8.setText(String.valueOf(totalPesoNeto()));
               form3.jTextField9.setText(String.valueOf(totalpagar()));
               tablaRecivos.setValueAt("ATENDIDO", select, 6);
              
            }
        } catch (HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"NO SE COMPLETO LA ACCION","ERROR",JOptionPane.ERROR_MESSAGE);
            
        }
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

