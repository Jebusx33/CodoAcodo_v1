  public int totalsaco(){
        int suma=0;
        for (int i = 0; i < z.tablaRemicion.getRowCount(); i++) {
            suma=suma+Integer.parseInt(z.tablaRemicion.getValueAt(i, 1).toString());
        }
        return suma;
    }
    
    
